// ---- i18n Engine ----
function setLanguage(lang) {
    if (!translations[lang]) return;

    // Merge page-specific translations (e.g. city/blog pages)
    if (window._cityTranslations) {
        Object.keys(window._cityTranslations).forEach(l => {
            if (translations[l]) {
                Object.assign(translations[l], window._cityTranslations[l]);
            }
        });
        window._cityTranslations = null; // only merge once
    }

    // Capture original EN text from HTML on first run (for keys not in translations.en)
    if (!window._enCaptured) {
        window._enCaptured = true;
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (!translations.en[key]) {
                translations.en[key] = el.textContent;
            }
        });
        document.querySelectorAll('[data-i18n-html]').forEach(el => {
            const key = el.getAttribute('data-i18n-html');
            if (!translations.en[key]) {
                translations.en[key] = el.innerHTML;
            }
        });
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (!translations.en[key]) {
                translations.en[key] = el.placeholder;
            }
        });
    }

    localStorage.setItem('galaxy-lang', lang);

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    // Update all elements with data-i18n-html attribute (for innerHTML)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        if (translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Update active flag button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        }
    });

    // Update HTML lang attribute
    document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('galaxy-lang') || 'en';
    setLanguage(savedLang);

    // Bind language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(btn.dataset.lang);
        });
    });
});
